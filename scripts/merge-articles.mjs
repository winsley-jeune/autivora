#!/usr/bin/env node
// Merge content-drafts/*.json into lib/blog-competitive.ts (sorted, validated).
// Run: node scripts/merge-articles.mjs
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const DRAFTS = join(ROOT, 'content-drafts');
const OUT = join(ROOT, 'lib', 'blog-competitive.ts');

const REQUIRED = ['slug', 'title', 'metaTitle', 'metaDescription', 'date', 'readTime', 'category', 'excerpt', 'content'];
// Publication order: comparisons first (highest intent), then problem mid-tail, then B2B verticals.
const ORDER = [
  'pura-vs-drift-real-cost-comparison',
  'pura-car-alternatives-no-subscription',
  'is-pura-worth-it-cost-calculator',
  'aroma360-vs-pura-vs-aromatech',
  'aromini-bt-vs-aera-vs-hotel-collection-studio-pro',
  'car-scent-subscription-cost-comparison',
  'tesla-ev-car-diffuser-guide',
  'commercial-scenting-cost-per-month',
  'how-long-does-diffuser-oil-last',
  'are-fragrance-oils-safe-for-pets',
  'salon-spa-scenting-guide',
  'dental-office-scenting-guide',
  'gym-scenting-guide',
  'restaurant-bar-scenting-guide',
  'boutique-retail-scenting-guide',
  'real-estate-staging-scent-guide',
  'airbnb-scenting-guide',
];

const articles = [];
const problems = [];
for (const file of readdirSync(DRAFTS).filter((f) => f.endsWith('.json')).sort()) {
  const raw = readFileSync(join(DRAFTS, file), 'utf8');
  let a;
  try {
    a = JSON.parse(raw);
  } catch (e) {
    problems.push(`${file}: invalid JSON — ${e.message}`);
    continue;
  }
  const missing = REQUIRED.filter((k) => !(k in a));
  if (missing.length) {
    problems.push(`${file}: missing ${missing.join(', ')}`);
    continue;
  }
  if (!a.metaTitle.endsWith('| Autivora')) problems.push(`${file}: metaTitle lacks brand suffix`);
  if (a.metaTitle.length > 60) problems.push(`${file}: metaTitle ${a.metaTitle.length} chars (>60)`);
  if (a.metaDescription.length > 160) problems.push(`${file}: metaDescription ${a.metaDescription.length} chars (>160)`);
  if (!Array.isArray(a.content) || a.content.some((b) => typeof b !== 'string')) {
    problems.push(`${file}: content must be string[]`);
    continue;
  }
  // Drop private fields (e.g. _calculator research payloads).
  const clean = Object.fromEntries(Object.entries(a).filter(([k]) => REQUIRED.includes(k)));
  articles.push(clean);
}

articles.sort((x, y) => {
  const ix = ORDER.indexOf(x.slug);
  const iy = ORDER.indexOf(y.slug);
  return (ix === -1 ? 999 : ix) - (iy === -1 ? 999 : iy);
});

const dupes = articles.map((a) => a.slug).filter((s, i, arr) => arr.indexOf(s) !== i);
if (dupes.length) problems.push(`duplicate slugs: ${dupes.join(', ')}`);

const banner = `// AUTO-GENERATED by scripts/merge-articles.mjs — do not edit by hand.
// Competitive/comparison SEO article set; regenerate from content-drafts/.
import type { BlogArticle } from './blog-data';

export const COMPETITIVE_ARTICLES: BlogArticle[] = `;
writeFileSync(OUT, banner + JSON.stringify(articles, null, 2) + ';\n');

console.log(`merged ${articles.length} articles → lib/blog-competitive.ts`);
for (const a of articles) console.log(`  ${a.slug} (${a.category}, ${a.readTime})`);
if (problems.length) {
  console.log('\nWARNINGS:');
  for (const p of problems) console.log('  ' + p);
}
